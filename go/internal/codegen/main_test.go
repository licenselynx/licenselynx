func TestGeneratorProducesCompilableFiles(t *testing.T) {
	t.Parallel()

	outputDir := t.TempDir()
	if err := run([]string{"-input", fixturePath(t), "-output", outputDir}); err != nil {
		t.Fatalf("run generator: %v", err)
	}

	expectedFiles := []string{
		"stable_map_generated.go",
		"risky_map_generated.go",
		"organizations_generated.go",
		"org_index_generated.go",
		"org_acme_corp_map_generated.go",
		"org_siemens_map_generated.go",
	}

	for _, filename := range expectedFiles {
		filePath := filepath.Join(outputDir, filename)
		content, err := os.ReadFile(filePath)
		if err != nil {
			t.Fatalf("read %s: %v", filename, err)
		}

		assertContains(t, string(content), "SPDX-License-Identifier: BSD-3-Clause")
		assertContains(t, string(content), "Code generated by go/internal/codegen. DO NOT EDIT.")
	}

	organizationsSource, err := os.ReadFile(filepath.Join(outputDir, "organizations_generated.go"))
	if err != nil {
		t.Fatalf("read organizations file: %v", err)
	}
	assertContains(t, string(organizationsSource), "OrgAcmeCorp Organization = \"acme-corp\"")
	assertContains(t, string(organizationsSource), "OrgSiemens  Organization = \"siemens\"")

	stableMapSource, err := os.ReadFile(filepath.Join(outputDir, "stable_map_generated.go"))
	if err != nil {
		t.Fatalf("read stable map file: %v", err)
	}
	assertContains(t, string(stableMapSource), "\"Apache 2.0\": {ID: \"Apache-2.0\", Src: \"spdx\"},")
	assertContains(t, string(stableMapSource), "\"MIT\":")
	assertContains(t, string(stableMapSource), "ID: \"MIT\", Src: \"spdx\"}")

	writeSupportFiles(t, outputDir)
	runGoBuild(t, outputDir)
	ensureFormatted(t, outputDir)
}

