func TestMainWritesErrorAndExits(t *testing.T) {
	t.Parallel()

	if os.Getenv("LICENSELYNX_CODEGEN_MAIN_TEST") == "1" {
		os.Args = []string{"codegen"}
		main()
		return
	}

	cmd := exec.Command(os.Args[0], "-test.run=TestMainWritesErrorAndExits")
	cmd.Env = append(os.Environ(), "LICENSELYNX_CODEGEN_MAIN_TEST=1")
	output, err := cmd.CombinedOutput()

	var exitErr *exec.ExitError
	if !errors.As(err, &exitErr) {
		t.Fatalf("expected subprocess to exit with error, got %v", err)
	}
	if exitErr.ExitCode() != 1 {
		t.Fatalf("unexpected exit code: got %d", exitErr.ExitCode())
	}
	assertContains(t, string(output), "code generation failed: missing required -input")
}

func fixturePath(t *testing.T) string {
	t.Helper()
	return filepath.Join("..", "..", "testdata", "merged_data.json")
}

func writeSupportFiles(t *testing.T, outputDir string) {
	t.Helper()

	files := map[string]string{
		"go.mod": "module generatedtest\n\ngo 1.25\n",
		"support.go": `package licenselynx

type LicenseObject struct {
	ID  string
	Src string
}

type Organization string
`,
	}

	for name, content := range files {
		if err := os.WriteFile(filepath.Join(outputDir, name), []byte(content), 0o644); err != nil {
			t.Fatalf("write %s: %v", name, err)
		}
	}
}

func runGoBuild(t *testing.T, outputDir string) {
	t.Helper()

	cmd := exec.Command("go", "build", "./...")
	cmd.Dir = outputDir
	output, err := cmd.CombinedOutput()
	if err != nil {
		t.Fatalf("go build failed: %v\n%s", err, output)
	}
}

func ensureFormatted(t *testing.T, outputDir string) {
	t.Helper()

	cmd := exec.Command("gofmt", "-d", ".")
	cmd.Dir = outputDir
	output, err := cmd.CombinedOutput()
	if err != nil {
		t.Fatalf("gofmt -d failed: %v\n%s", err, output)
	}
	if len(output) != 0 {
		t.Fatalf("generated files are not gofmt clean:\n%s", output)
	}
}

func assertContains(t *testing.T, content, want string) {
	t.Helper()
	if !strings.Contains(content, want) {
		t.Fatalf("expected content to contain %q\nactual:\n%s", want, content)
	}
}
