func TestIdentifierSuffix(t *testing.T) {
	t.Parallel()

	tests := []struct {
		name  string
		input string
		want  string
	}{
		{name: "simple org", input: "siemens", want: "Siemens"},
		{name: "mixed separators", input: "acme-corp_group", want: "AcmeCorpGroup"},
		{name: "starts with digit", input: "123-org", want: "X123Org"},
		{name: "punctuation only", input: "---", want: "Org"},
		{name: "empty", input: "", want: "Org"},
	}

	for _, test := range tests {
		t.Run(test.name, func(t *testing.T) {
			t.Parallel()
			if got := identifierSuffix(test.input); got != test.want {
				t.Fatalf("identifierSuffix(%q) = %q, want %q", test.input, got, test.want)
			}
		})
	}
}

